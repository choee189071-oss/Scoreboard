def census_profile_population(year, deal_setup, census_api_key=None, us=False):
    """Pull ACS 5-year profile population DP05_0001E for local geography or U.S.

    This supports county/place because those are already in the deal setup.
    Returns {population, name, source_url, year}.
    """
    year = int(year)
    base_url = f"https://api.census.gov/data/{year}/acs/acs5/profile"
    params = {"get": "NAME,DP05_0001E"}
    if census_api_key:
        params["key"] = census_api_key

    if us:
        params["for"] = "us:*"
    else:
        state_fips = str(deal_setup.get("state_fips", "")).zfill(2)
        geo_code = str(deal_setup.get("county_or_place_fips", ""))
        geography_type = str(deal_setup.get("geography_type", "county")).lower()
        if geography_type == "place":
            params["for"] = f"place:{geo_code.zfill(5)}"
            params["in"] = f"state:{state_fips}"
        else:
            params["for"] = f"county:{geo_code.zfill(3)}"
            params["in"] = f"state:{state_fips}"

    response = requests.get(base_url, params=params, timeout=20)
    response.raise_for_status()
    payload = response.json()
    if len(payload) < 2:
        raise ValueError("Census response returned no population rows.")

    headers = payload[0]
    row = payload[1]
    data = dict(zip(headers, row))
    pop = float(data.get("DP05_0001E"))

    return {
        "population": pop,
        "name": data.get("NAME", "United States" if us else "Local geography"),
        "year": year,
        "source_url": response.url,
    }


def run_population_growth_difference_engine(deal_setup, census_api_key=None):
    """Auto-pull local vs U.S. population growth and write directly to approved inputs.

    This is objective public data, so it can be Auto Pulled. The scorecard receives:
    population_growth_difference_vs_us = local_growth_pct - us_growth_pct.
    """
    current_year = int(deal_setup.get("census_year", 2023))
    base_year = max(2010, current_year - 5)

    try:
        local_now = census_profile_population(current_year, deal_setup, census_api_key=census_api_key, us=False)
        local_then = census_profile_population(base_year, deal_setup, census_api_key=census_api_key, us=False)
        us_now = census_profile_population(current_year, deal_setup, census_api_key=census_api_key, us=True)
        us_then = census_profile_population(base_year, deal_setup, census_api_key=census_api_key, us=True)

        local_growth = ((local_now["population"] / local_then["population"]) - 1.0) * 100.0
        us_growth = ((us_now["population"] / us_then["population"]) - 1.0) * 100.0
        diff = round(local_growth - us_growth, 2)

        safe_add_approved_input(
            scorecard_key="population_growth_difference_vs_us",
            value=diff,
            status="Auto Pulled",
            source_method="Census ACS Population Growth Engine",
            source_document=f"ACS {base_year} and {current_year} DP05_0001E",
            confidence=1.0,
            notes=(
                f"Local growth {local_growth:.2f}% vs U.S. growth {us_growth:.2f}% "
                f"from ACS {base_year} to {current_year}."
            ),
        )

        upsert_auto_data_result_by_target(
            {
                "data_source": "Census / ACS",
                "series_label": f"Population growth {base_year}–{current_year}",
                "target_data": "Population Growth Difference vs U.S.",
                "status": "success",
                "date_or_year": f"{base_year}-{current_year}",
                "value": f"{diff:.2f}%",
                "source_url": local_now.get("source_url", ""),
                "notes": f"{local_now.get('name', 'Local geography')}: {local_growth:.2f}%; U.S.: {us_growth:.2f}%.",
            },
            target_contains="Population Growth Difference",
            unique_key="population_growth_difference_acs",
        )
        return {"ok": True, "value": diff, "local_growth": local_growth, "us_growth": us_growth}

    except Exception as e:
        upsert_auto_data_result_by_target(
            {
                "data_source": "Census / ACS",
                "series_label": "Population growth",
                "target_data": "Population Growth Difference vs U.S.",
                "status": "needs_review",
                "date_or_year": f"{base_year}-{current_year}",
                "value": "",
                "source_url": "https://api.census.gov/data.html",
                "notes": f"Population growth auto-pull failed: {e}",
            },
            target_contains="Population Growth Difference",
            unique_key="population_growth_difference_acs",
        )
        return {"ok": False, "error": str(e)}


def infer_msa_participation_candidate(deal_setup):
    """Create a reviewable MSA Participation candidate from geography context.

    This is intentionally an inference, not a hard data pull. It should always require
    analyst approval in Reliability Review.
    """
    location = str(deal_setup.get("location_name") or "").strip()
    county = str(deal_setup.get("county_name") or "").strip()
    state = str(deal_setup.get("state") or "").strip()
    combined = f"{location} {county} {state}".lower()

    broad_terms = [
        "los angeles", "long beach", "anaheim", "orange county", "san francisco", "oakland", "san jose",
        "new york", "newark", "jersey city", "chicago", "dallas", "fort worth", "houston", "austin",
        "seattle", "boston", "washington", "arlington", "alexandria", "denver", "phoenix", "miami",
        "atlanta", "philadelphia", "san diego", "riverside", "sacramento",
    ]

    if not location and not county:
        value = "No"
        confidence = 0.45
        reason = "No MSA/county context was provided; analyst should verify regional participation."
    elif any(term in combined for term in broad_terms):
        value = "Yes; Broad & Diverse"
        confidence = 0.78
        reason = "The deal geography appears to participate in a large/diverse metropolitan economy based on the location/county fields."
    else:
        value = "Yes; Not Broad & Diverse"
        confidence = 0.62
        reason = "The deal has a named local/regional economy, but the app cannot confirm broad/diverse participation from geography alone."

    notes = (
        f"Rule/AI-assisted inference from location='{location}', county='{county}', state='{state}'. "
        f"{reason} Double-check employment base, commuter patterns, and MSA linkages before approval."
    )

    upsert_candidate_value(
        scorecard_key="msa_participation",
        scorecard_field="MSA Participation",
        value=value,
        source_method="MSA Participation Inference Engine",
        confidence=confidence,
        notes=notes,
        source_document="Deal setup geography fields",
        source_url="",
    )

    upsert_auto_data_result_by_target(
        {
            "data_source": "AI / Rule-Based Inference",
            "series_label": "MSA participation candidate",
            "target_data": "MSA Participation",
            "status": "needs_review",
            "date_or_year": datetime.now().date().isoformat(),
            "value": value,
            "source_url": "",
            "notes": notes,
        },
        target_contains="MSA Participation",
        unique_key="msa_participation_inference",
    )
    return {"ok": True, "value": value, "confidence": confidence, "notes": notes}


def get_session_float(keys, default=None):
    for key in keys:
        if key in st.session_state:
            try:
                return float(str(st.session_state.get(key)).replace("%", "").replace(",", ""))
            except Exception:
                pass
    return default


def extract_auto_result_percent(target_contains):
    target_contains = str(target_contains).lower()
    for r in st.session_state.get("auto_data_results", []) or []:
        target = str(r.get("target_data") or r.get("Target Data") or "").lower()
        if target_contains in target:
            raw = str(r.get("value") or r.get("Extracted / Calculated Value") or "")
            m = re.search(r"[-+]?\d+(?:\.\d+)?", raw)
            if m:
                try:
                    return float(m.group(0))
                except Exception:
                    pass
    return None


def generate_real_estate_volatility_candidate_from_available_data(key_prefix="data_workspace"):
    """Generate the Real Estate Market Volatility candidate from pulled housing indicators.

    Uses the existing deterministic housing classifier. This does not approve the value;
    it only pushes a candidate into Reliability Review.
    """
    price_yoy = get_session_float(
        [f"{key_prefix}_auto_home_price_yoy", "main_auto_home_price_yoy", "data_workspace_auto_home_price_yoy"],
        default=extract_auto_result_percent("home price yoy"),
    )
    inventory_yoy = get_session_float(
        [f"{key_prefix}_auto_inventory_yoy", "main_auto_inventory_yoy", "data_workspace_auto_inventory_yoy"],
        default=extract_auto_result_percent("inventory yoy"),
    )
    distress_proxy = get_session_float(
        [f"{key_prefix}_auto_distress_proxy", "main_auto_distress_proxy", "data_workspace_auto_distress_proxy"],
        default=extract_auto_result_percent("distress proxy"),
    )
    affordability_worse = bool(st.session_state.get(f"{key_prefix}_auto_affordability_worse", True))

    if price_yoy is None and inventory_yoy is None and distress_proxy is None:
        return {"ok": False, "error": "No pulled housing indicators available yet. Run Housing Market Context Pull first."}

    # If observed distress is missing, use the already designed safe inferred fallback.
    inferred_note = ""
    if distress_proxy is None:
        inferred = infer_distress_proxy_from_indicators(
            price_yoy=price_yoy or 0.0,
            inventory_yoy=inventory_yoy or 0.0,
            local_unemployment_diff=get_local_unemployment_diff_from_session(),
            affordability_worse=affordability_worse,
        )
        distress_proxy = inferred["distress_proxy"]
        inferred_note = " Inferred distress proxy used: " + "; ".join(inferred.get("reasons", []))

    housing = classify_housing_market_proxy(
        price_yoy=price_yoy or 0.0,
        inventory_yoy=inventory_yoy or 0.0,
        distress_proxy=distress_proxy or 0.0,
        affordability_worse=affordability_worse,
    )

    notes = (
        f"Derived from available market indicators: home price YoY={price_yoy}, inventory YoY={inventory_yoy}, "
        f"distress proxy={distress_proxy}, affordability_worse={affordability_worse}. "
        f"Reasons: {'; '.join(housing['reasons'])}.{inferred_note} Double-check source links before approval."
    )

    upsert_candidate_value(
        scorecard_key="real_estate_market_volatility",
        scorecard_field="Real Estate Market Volatility",
        value=housing["classification"],
        source_method="Real Estate Volatility Calculation Engine",
        confidence=housing["confidence"],
        notes=notes,
        source_document="FHFA/Realtor/AI Source Scout or analyst override market indicators",
        source_url="",
    )

    upsert_auto_data_result_by_target(
        {
            "data_source": "Derived Housing Market Engine",
            "series_label": "Real estate market volatility candidate",
            "target_data": "Real Estate Market Volatility",
            "status": "needs_review",
            "date_or_year": datetime.now().date().isoformat(),
            "value": housing["classification"],
            "source_url": "",
            "notes": notes,
        },
        target_contains="Real Estate Market Volatility",
        unique_key="real_estate_volatility_derived",
    )
    return {"ok": True, **housing, "notes": notes}



def ensure_real_estate_volatility_candidate_synced():
    """Make sure housing/market-context rows map into the scorecard field.

    This fixes the common case where Auto Context Results contains housing
    indicators or a housing proxy candidate, but the Economic Fundamentals card
    still shows Real Estate Market Volatility as Missing.

    The function is conservative:
    - approved analyst/manual values win;
    - existing reviewable candidates win;
    - a direct housing-classification row is reused when present;
    - otherwise, if pulled housing indicators exist, it generates a derived
      Needs Review candidate using the deterministic housing classifier.
    """
    key = "real_estate_market_volatility"

    approved = st.session_state.get("approved_inputs", {}) or {}
    if key in approved and not is_missing_master_value(approved.get(key)):
        return None

    existing_candidates = st.session_state.get("candidate_extractions", []) or []
    for c in existing_candidates:
        if c.get("scorecard_key") == key and has_candidate_value(c.get("value")):
            return c

    # 1) Reuse an explicit classification row if Auto Context Results already has one.
    for item in st.session_state.get("auto_data_results", []) or []:
        target = str(item.get("target_data") or item.get("Target Data") or "").lower()
        raw_status = str(item.get("status") or item.get("Status") or "").lower()
        value = (
            item.get("value")
            or item.get("Extracted / Calculated Value")
            or item.get("extracted_calculated_value")
            or item.get("calculated_value")
        )

        is_housing_classification = (
            "real estate market volatility" in target
            or "housing market trend" in target
            or "housing market volatility" in target
        )

        if is_housing_classification and has_candidate_value(value) and raw_status not in {"manual_required", "missing"}:
            return upsert_candidate_value(
                scorecard_key=key,
                scorecard_field="Real Estate Market Volatility",
                value=value,
                source_method=item.get("data_source") or item.get("Data Source") or "Housing Market Context Engine",
                confidence=item.get("confidence") or item.get("Confidence") or 0.70,
                notes=item.get("notes", "Housing classification candidate from Auto Context Results. Double-check sources before approval."),
                source_document=item.get("series_label") or item.get("Series / Label") or "Housing market context",
                source_url=item.get("source_url") or item.get("Source URL") or "",
            )

    # 2) If only raw indicators exist, generate a derived candidate.
    has_indicator = False
    indicator_terms = ["home price yoy", "inventory yoy", "distress proxy"]
    for item in st.session_state.get("auto_data_results", []) or []:
        target = str(item.get("target_data") or item.get("Target Data") or "").lower()
        value = item.get("value") or item.get("Extracted / Calculated Value") or item.get("calculated_value")
        if any(term in target for term in indicator_terms) and has_candidate_value(value):
            has_indicator = True
            break

    if has_indicator:
        try:
            return generate_real_estate_volatility_candidate_from_available_data(key_prefix="data_workspace")
        except Exception as exc:
            st.session_state.setdefault("economic_engine_warnings", []).append(
                f"Real estate volatility sync could not generate candidate: {exc}"
            )
            return None

    return None

def run_economic_fundamentals_engines(deal_setup, census_api_key=None, key_prefix="data_workspace"):
    """Fill the three previously-empty Economic Fundamental fields using their proper method.

    - Population Growth Difference: direct Census ACS pull/calculation, auto-approved.
    - MSA Participation: AI/rule-based inference candidate, analyst approval required.
    - Real Estate Market Volatility: derived from pulled housing indicators if available, analyst approval required.
    """
    results = {}
    results["population_growth"] = run_population_growth_difference_engine(deal_setup, census_api_key=census_api_key)
    results["msa_participation"] = infer_msa_participation_candidate(deal_setup)
    results["real_estate_volatility"] = generate_real_estate_volatility_candidate_from_available_data(key_prefix=key_prefix)
    sync_candidates_from_sources()
    return results


def build_current_source_status_table():
    """
    Starts from source_priority_table(), but replaces manual placeholders with live approved values
    when the app has successfully pulled/calculated data. This avoids duplicate confusing rows.
    """
    try:
        df = source_priority_table().copy()
    except Exception:
        return pd.DataFrame()

    approved = st.session_state.get("approved_inputs", {}) or {}
    meta = st.session_state.get("approved_input_metadata", {}) or {}

    def set_row_by_target(target_contains, status, value, source_url="", date_year="", source_method=""):
        nonlocal df
        target_cols = [c for c in df.columns if "target" in str(c).lower() or "data" in str(c).lower() or "field" in str(c).lower()]
        if not target_cols:
            return
        mask = pd.Series(False, index=df.index)
        for col in target_cols:
            mask = mask | df[col].astype(str).str.contains(target_contains, case=False, na=False)
        if not mask.any():
            return
        if "Status" in df.columns:
            df.loc[mask, "Status"] = status
        elif "status" in df.columns:
            df.loc[mask, "status"] = status
        if "Extracted / Calculated Value" in df.columns:
            df.loc[mask, "Extracted / Calculated Value"] = value
        if "Date / Year" in df.columns:
            df.loc[mask, "Date / Year"] = date_year
        if "Source URL" in df.columns:
            df.loc[mask, "Source URL"] = source_url
        elif "source_url" in df.columns:
            df.loc[mask, "source_url"] = source_url
        if source_method:
            for col in df.columns:
                if "source" in str(col).lower() and "url" not in str(col).lower():
                    df.loc[mask, col] = source_method
                    break

    # 7. Local unemployment: auto-fill the original BLS/LAUS row if approved value exists.
    key = "unemployment_rate_difference_vs_us"
    if key in approved and not is_missing_master_value(approved.get(key)):
        m = meta.get(key, {}) or {}
        set_row_by_target(
            "Local unemployment",
            "success",
            f"{approved.get(key)} %",
            source_url="https://fred.stlouisfed.org/",
            date_year=display_value(m.get("review_timestamp", ""))[:10],
            source_method=display_value(m.get("source_method", "BLS / LAUS via FRED")),
        )

    # 8. Assessed value: show parser result if user approved/uploaded it.
    key = "total_assessed_value"
    if key in approved and not is_missing_master_value(approved.get(key)):
        m = meta.get(key, {}) or {}
        try:
            value = f"${float(approved.get(key)):,.0f}"
        except Exception:
            value = approved.get(key)
        set_row_by_target(
            "Assessed value",
            "success",
            value,
            source_url="",
            date_year=display_value(m.get("review_timestamp", ""))[:10],
            source_method=display_value(m.get("source_method", "Assessed Value Upload Parser")),
        )

    # 9. Housing proxy: show candidate/approved status if available.
    key = "real_estate_market_volatility"
    if key in approved and not is_missing_master_value(approved.get(key)):
        m = meta.get(key, {}) or {}
        set_row_by_target(
            "Housing market trend|distress proxy|Real Estate Market Volatility",
            display_value(m.get("status", "success")) or "success",
            approved.get(key),
            source_url="",
            date_year=display_value(m.get("review_timestamp", ""))[:10],
            source_method=display_value(m.get("source_method", "Housing Market Proxy Classifier")),
        )

    return clean_display_dataframe(df)



def extract_assessed_value_candidates_from_documents(parsed_docs):
    """
    Full-document OS / Appendix assessed-value resolver.

    This deliberately scans the entire parsed OS text and uses broad language patterns,
    because CFD official statements often phrase the value as:
    - preliminary assessed value
    - assessed value of all taxable property
    - Fiscal Year XXXX assessed value of all taxable property
    - total assessed valuation / taxable assessed value

    The function only creates candidates; analyst approval is still required.
    """
    candidates = []

    # Higher priority = better candidate when several assessed-value numbers are found.
    patterns = [
        {
            "label": "All taxable property assessed value",
            "priority": 100,
            "confidence": 0.86,
            "pattern": r"assessed\s+value\s+of\s+all\s+(?:the\s+)?taxable\s+property[^$0-9]{0,260}\$?\s*([0-9][0-9,]{5,}(?:\.\d+)?)",
        },
        {
            "label": "Fiscal-year assessed value of all taxable property",
            "priority": 98,
            "confidence": 0.86,
            "pattern": r"fiscal\s+year\s+\d{4}(?:-\d{2})?[^.]{0,300}?assessed\s+value\s+of\s+all\s+(?:the\s+)?taxable\s+property[^$0-9]{0,260}\$?\s*([0-9][0-9,]{5,}(?:\.\d+)?)",
        },
        {
            "label": "Total assessed value",
            "priority": 92,
            "confidence": 0.82,
            "pattern": r"(?:total|aggregate)\s+assessed\s+value[^$0-9]{0,240}\$?\s*([0-9][0-9,]{5,}(?:\.\d+)?)",
        },
        {
            "label": "Taxable assessed value",
            "priority": 90,
            "confidence": 0.80,
            "pattern": r"taxable\s+assessed\s+value[^$0-9]{0,240}\$?\s*([0-9][0-9,]{5,}(?:\.\d+)?)",
        },
        {
            "label": "Total assessed valuation",
            "priority": 88,
            "confidence": 0.78,
            "pattern": r"(?:total|aggregate)?\s*assessed\s+valuation[^$0-9]{0,240}\$?\s*([0-9][0-9,]{5,}(?:\.\d+)?)",
        },
        {
            "label": "Preliminary assessed value",
            "priority": 74,
            "confidence": 0.72,
            "pattern": r"preliminary\s+assessed\s+value[^$0-9]{0,300}\$?\s*([0-9][0-9,]{5,}(?:\.\d+)?)",
        },
        {
            "label": "Developed property assessed value",
            "priority": 70,
            "confidence": 0.70,
            "pattern": r"developed\s+property[^.]{0,320}?assessed\s+value[^$0-9]{0,240}\$?\s*([0-9][0-9,]{5,}(?:\.\d+)?)",
        },
        {
            "label": "Assessor office assessed value",
            "priority": 68,
            "confidence": 0.70,
            "pattern": r"assessor(?:'s)?\s+office[^.]{0,320}?assessed\s+value[^$0-9]{0,240}\$?\s*([0-9][0-9,]{5,}(?:\.\d+)?)",
        },
        {
            "label": "Full cash value",
            "priority": 58,
            "confidence": 0.62,
            "pattern": r"full\s+cash\s+value[^$0-9]{0,180}\$?\s*([0-9][0-9,]{5,}(?:\.\d+)?)",
        },
        {
            "label": "Generic assessed value",
            "priority": 45,
            "confidence": 0.58,
            "pattern": r"assessed\s+value[^$0-9]{0,180}\$?\s*([0-9][0-9,]{5,}(?:\.\d+)?)",
        },
    ]

    vtl_ratio_pattern = r"(?:estimated\s+)?assessed\s+value\s*[- ]?to\s*[- ]?lien\s+ratio[^0-9]{0,160}(\d+(?:\.\d+)?)\s*(?:-?to-?1|x|:1)"

    for doc in parsed_docs or []:
        text = doc.get("text", "") or ""
        if not text.strip():
            continue

        # Keep the full OS, not just the first N characters. Normalize whitespace
        # so sentence-spanning patterns can match across line breaks.
        compact = re.sub(r"\s+", " ", text)
        source_doc = doc.get("file_name", "Uploaded document")

        for spec in patterns:
            for m in re.finditer(spec["pattern"], compact, flags=re.IGNORECASE | re.DOTALL):
                raw = m.group(1)
                val = parse_numeric_input(raw, default=0.0)
                if val <= 0 or val < 100000:
                    continue

                start = max(0, m.start() - 300)
                end = min(len(compact), m.end() + 300)
                excerpt = compact[start:end]

                ratio_match = re.search(vtl_ratio_pattern, excerpt, flags=re.IGNORECASE | re.DOTALL)
                ratio = float(ratio_match.group(1)) if ratio_match else None

                candidates.append({
                    "value": float(val),
                    "source_document": source_doc,
                    "excerpt": excerpt,
                    "confidence": float(spec["confidence"]),
                    "raw_value": raw,
                    "match_label": spec["label"],
                    "priority": int(spec["priority"]),
                    "nearby_value_to_lien_ratio": ratio,
                })

    # Deduplicate by value/document/label and prefer more specific phrases.
    seen = set()
    deduped = []
    for c in sorted(candidates, key=lambda x: (x.get("priority", 0), x.get("confidence", 0), x.get("value", 0)), reverse=True):
        key = (round(c["value"], 0), c["source_document"], c.get("match_label", ""))
        if key not in seen:
            seen.add(key)
            deduped.append(c)

    # If an "all taxable property" candidate exists, surface it first. This is usually
    # the best total tax-base input for the VTL/tax-base workflow.
    deduped = sorted(
        deduped,
        key=lambda x: (
            1 if "all taxable" in str(x.get("match_label", "")).lower() else 0,
            x.get("priority", 0),
            x.get("confidence", 0),
            x.get("value", 0),
        ),
        reverse=True,
    )
    return deduped[:12]

