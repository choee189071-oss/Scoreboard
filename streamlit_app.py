def add_approved_input(
    scorecard_key,
    value,
    status,
    source_method,
    source_document=None,
    confidence=None,
    notes=None,
):
    if value is None:
        return

    st.session_state["approved_inputs"][scorecard_key] = value
    st.session_state["approved_input_metadata"][scorecard_key] = {
        "status": status,
        "source_method": source_method,
        "source_document": source_document,
        "confidence": confidence,
        "reviewed_by": "Analyst" if status in ["AI Extracted", "Manual Input"] else None,
        "review_timestamp": datetime.now().isoformat(timespec="seconds"),
        "notes": notes,
    }


def merge_approved_inputs(new_inputs: dict, status="Auto Pulled", source_method="Auto Data Connector"):
    for key, value in (new_inputs or {}).items():
        safe_add_approved_input(
            scorecard_key=key,
            value=value,
            status=status,
            source_method=source_method,
            confidence=1.0 if status == "Auto Pulled" else None,
            notes=f"{status} value.",
        )


def candidate_row_from_auto_result(item):
    target = item.get("target_data") or item.get("Target Data")
    value = (
        item.get("median_household_ebi_percent_of_us")
        or item.get("value")
        or item.get("Extracted / Calculated Value")
    )

    scorecard_key = None
    scorecard_field = target

