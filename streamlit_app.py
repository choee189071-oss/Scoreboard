def infer_scorecard_value_from_context(scorecard_key, deal_setup=None):
    """Rule-based AI-style inference used only when verified source data is unavailable.

    This intentionally stays conservative and returns warnings so users know the value
    is not source-verified. Analysts must approve before the value enters the scorecard.
    """
    deal_setup = deal_setup or st.session_state.get("deal_setup", {}) or {}
    approved = st.session_state.get("approved_inputs", {}) or {}
    issuer_name = str(deal_setup.get("issuer_name", "")).lower()
    county_name = str(deal_setup.get("county_name", "")).lower()
    location_name = str(deal_setup.get("location_name", "")).lower()

    parcel_count = _fallback_safe_float(approved.get("district_size_parcels"), None)
    assessed_value = _fallback_safe_float(st.session_state.get("approved_inputs", {}).get("assessed_value"), None)
    vtl = _fallback_safe_float(approved.get("est_value_to_lien"), None)
    mltm = _fallback_safe_float(approved.get("maximum_loss_to_maturity_percent"), None)

    master_planned_signal = any(term in issuer_name for term in ["mission viejo", "rancho", "ranch", "cfd", "community facilities"])
    broad_msa_signal = any(term in location_name for term in ["los angeles", "anaheim", "orange", "san francisco", "san diego"])

    if scorecard_key == "top10_taxpayers_percent_of_total_levy":
        if parcel_count and parcel_count >= 5000:
            value, confidence = 25.0, 0.68
            rationale = "Large parcel count suggests a broader tax base, so concentration is inferred below the sector median."
        elif parcel_count and parcel_count >= 1000:
            value, confidence = 35.0, 0.62
            rationale = "Moderate-to-large parcel count suggests approximately sector-median taxpayer concentration."
        elif master_planned_signal:
            value, confidence = 45.0, 0.58
            rationale = "CFD/master-planned development signal suggests possible developer concentration; using conservative inferred concentration."
        else:
            value, confidence = 35.0, 0.50
            rationale = "Insufficient deal-specific taxpayer data; using a conservative AI-inferred sector-like estimate."
        return {"value": value, "confidence": confidence, "rationale": rationale}

    if scorecard_key == "largest_taxpayer_percent_of_total_levy":
        if parcel_count and parcel_count >= 5000:
            value, confidence = 5.0, 0.65
            rationale = "Large parcel count implies lower single-taxpayer concentration, absent contrary evidence."
        elif parcel_count and parcel_count >= 1000:
            value, confidence = 8.0, 0.60
            rationale = "Moderate-to-large parcel base implies a sector-median largest-taxpayer assumption."
        elif master_planned_signal:
            value, confidence = 15.0, 0.56
            rationale = "CFD/master-planned development signal may imply a larger developer taxpayer; using conservative inferred value."
        else:
            value, confidence = 8.0, 0.50
            rationale = "Insufficient source data; using a conservative AI-inferred estimate."
        return {"value": value, "confidence": confidence, "rationale": rationale}

    if scorecard_key == "msa_participation":
        if broad_msa_signal:
            return {
                "value": "Yes; Broad & Diverse",
                "confidence": 0.70,
                "rationale": "Location appears integrated with a large, diversified MSA; analyst should verify employment/commute context.",
            }
        return {
            "value": "Yes; Not Broad & Diverse",
            "confidence": 0.55,
            "rationale": "No strong broad-MSA signal found; using a moderate MSA-participation inference.",
        }

    if scorecard_key == "real_estate_market_volatility":
        # Prefer already-derived housing classification if available.
        for c in st.session_state.get("candidate_extractions", []) or []:
            if c.get("scorecard_key") == "real_estate_market_volatility" and has_candidate_value(c.get("value")):
                return {
                    "value": c.get("value"),
                    "confidence": min(float(c.get("confidence") or 0.65), 0.75),
                    "rationale": "Uses the existing housing proxy candidate from market data / AI source scout.",
                }
        return {
            "value": "Elevated Volatility; Stable Prices; Affordability Worse Than National Figures",
            "confidence": 0.55,
            "rationale": "No complete housing dataset found; using a conservative inferred housing-market classification.",
        }

    if scorecard_key == "conveyance_to_homeowners":
        if parcel_count and parcel_count >= 1000:
            return {
                "value": "Most Conveyed",
                "confidence": 0.62,
                "rationale": "Large parcel count suggests meaningful homeowner conveyance, but source verification is required.",
            }
        return {
            "value": "Fairly Developed with Significant Conveyance (Some Developer Concentration)",
            "confidence": 0.55,
            "rationale": "No direct conveyance table found; using conservative development-stage inference.",
        }

    # Numeric fallback-like fields
    sector_value = get_sector_median_value(scorecard_key, deal_setup.get("bond_type", "Special Assessment Debt"))
    if sector_value is not None:
        return {
            "value": sector_value,
            "confidence": 0.45,
            "rationale": "No source-verified value found; AI inference defaults toward sector median.",
        }

    return {"value": None, "confidence": 0.0, "rationale": "No inference rule available."}


