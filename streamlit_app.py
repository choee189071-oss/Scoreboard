def safe_value(value):
    if value is None:
        return None

    if isinstance(value, str) and value.strip() in ["", "None", "nan", "NaN"]:
        return None

    try:
        if pd.isna(value):
            return None
    except Exception:
        pass

    return value


def parse_numeric_input(value, default=0.0):
    """
    Converts common muni-credit extracted strings into float-safe values.
    Examples:
    - ">16:1" -> 16.0
    - "16-to-1" -> 16.0
    - "16x" -> 16.0
    - "13.9%" -> 13.9
    - "$1,234,000" -> 1234000.0
    """
    if value is None:
        return float(default)

    if isinstance(value, (int, float)):
        try:
            if pd.isna(value):
                return float(default)
        except Exception:
            pass
        return float(value)

    text = str(value).strip()

    if text in ["", "None", "nan", "NaN"]:
        return float(default)

    text = text.replace(",", "")
    text = text.replace("$", "")
    text = text.replace("%", "")
    text = text.replace("x", "")
    text = text.replace("X", "")
    text = text.replace("to-1", ":1")
    text = text.replace("-to-1", ":1")
    text = text.replace(" to 1", ":1")
    text = text.replace("to 1", ":1")

    # For ratio strings like >16:1 or 16:1, use the numerator.
    ratio_match = re.search(r"[-+]?\d*\.?\d+", text)
    if ratio_match:
        return float(ratio_match.group())

    return float(default)


def display_value(value):
    """
    UI-safe display formatter.
    Missing values should look blank in the analyst-facing reliability matrix,
    not like Python/debug values such as None, nan, or NaN.
    """
    if value is None:
        return ""

    try:
        if pd.isna(value):
            return ""
    except Exception:
        pass

    if isinstance(value, str):
        cleaned = value.strip()
        if cleaned.lower() in ["none", "nan", "null", "n/a"]:
            return ""
        return cleaned

    return value


def is_missing_master_value(value):
    """True when a master scorecard value should be treated as missing."""
    if value is None:
        return True

    try:
        if pd.isna(value):
            return True
    except Exception:
        pass

    if isinstance(value, str) and value.strip().lower() in ["", "none", "nan", "null", "n/a", "missing"]:
        return True

    return False




def has_candidate_value(value):
    """Return True only when an AI/source candidate has a usable value.

    Empty strings, None, NaN, placeholder words, and zero-confidence blank
    extraction shells should not create Needs Review cards.
    """
    if value is None:
        return False

    try:
        if pd.isna(value):
            return False
    except Exception:
        pass

    if isinstance(value, str):
        cleaned = value.strip()
        if cleaned == "":
            return False
        if cleaned.lower() in [
            "none",
            "nan",
            "null",
            "n/a",
            "na",
            "missing",
            "not found",
            "not available",
            "unknown",
        ]:
            return False

    return True

def get_master_value(scorecard_key, fallback=None):
    """
    Single source of truth reader.

    Priority:
    1. approved_inputs value if present
    2. fallback only if explicitly supplied

    This prevents old static defaults from appearing as if they were approved data.
    """
    approved = st.session_state.get("approved_inputs", {}) or {}

    if scorecard_key in approved and not is_missing_master_value(approved.get(scorecard_key)):
        return approved.get(scorecard_key)

    return fallback


def get_master_status(scorecard_key):
    meta = st.session_state.get("approved_input_metadata", {}) or {}
    return (meta.get(scorecard_key, {}) or {}).get("status", "Missing")


