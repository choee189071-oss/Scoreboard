def sync_candidates_from_sources():
    candidate_rows = []

    for item in st.session_state.get("auto_data_results", []) or []:
        row = candidate_row_from_auto_result(item)
        if row:
            candidate_rows.append(row)

    doc_result = st.session_state.get("last_document_ai_result")

    if doc_result and doc_result.get("ok"):
        extracted = doc_result.get("extracted_fields", []) or doc_result.get("candidates", [])

        for item in extracted:
            scorecard_key = item.get("scorecard_key") or item.get("field_key")
            scorecard_field = item.get("scorecard_field") or item.get("field") or scorecard_key
            value = item.get("value") or item.get("extracted_value") or item.get("numeric_value")
            confidence = item.get("confidence", 0.65)
            source_document = item.get("source_document") or item.get("document") or "Uploaded document"
            source_excerpt = item.get("source_excerpt") or item.get("excerpt") or item.get("evidence") or ""

            if scorecard_key:
                candidate_rows.append(
                    {
                        "scorecard_field": scorecard_field,
                        "scorecard_key": scorecard_key,
                        "value": value,
                        "status": "Needs Review",
                        "source_method": "AI Extraction",
                        "source_document": source_document,
                        "source_url": item.get("source_url", ""),
                        "confidence": confidence,
                        "notes": item.get("notes", ""),
                        "source_excerpt": source_excerpt,
                    }
                )

                if source_excerpt:
                    st.session_state["source_evidence"].append(
                        {
                            "field": scorecard_field,
                            "scorecard_key": scorecard_key,
                            "source_document": source_document,
                            "page": item.get("page", None),
                            "snippet": source_excerpt,
                            "confidence": confidence,
                        }
                    )

    st.session_state["candidate_extractions"] = candidate_rows
    return candidate_rows


def build_data_reliability_matrix():
    approved_inputs = st.session_state.get("approved_inputs", {}) or {}
    approved_meta = st.session_state.get("approved_input_metadata", {}) or {}
    candidates = st.session_state.get("candidate_extractions", []) or []

    rows = []

    for field in REQUIRED_SCORECARD_FIELDS:
        key = field["scorecard_key"]
        value = safe_value(approved_inputs.get(key))
        meta = approved_meta.get(key, {})

        field_candidates = [c for c in candidates if c.get("scorecard_key") == key]
        best_candidate = field_candidates[0] if field_candidates else None

        if value is not None:
            status = meta.get("status", "Manual Input")
            source_method = meta.get("source_method", "")
            source_document = meta.get("source_document", "")
            confidence = meta.get("confidence", None)
            notes = meta.get("notes", "")
        elif best_candidate:
            status = "Needs Review"
            value = best_candidate.get("value")
            source_method = best_candidate.get("source_method", "Candidate")
            source_document = best_candidate.get("source_document", "")
            confidence = best_candidate.get("confidence", None)
            notes = "Candidate exists but has not been approved."
        else:
            status = "Missing"
            value = None
            source_method = ""
            source_document = ""
            confidence = None
            notes = "No value available yet."

        rows.append(
            {
                "category": field["category"],
                "scorecard_field": field["scorecard_field"],
                "scorecard_key": key,
                "status": status,
                "status_icon": STATUS_OPTIONS.get(status, {}).get("icon", ""),
                "current_value": display_value(value),
                "source_method": display_value(source_method),
                "source_document": display_value(source_document),
                "confidence": display_value(confidence),
                "required": field["required"],
                "notes": display_value(notes),
            }
        )

    return pd.DataFrame(rows)


