    return {
        "ok": True,
        "inventory_yoy": round(float(yoy), 2),
        "matched_place": str(chosen_row[geo_col]),
        "date_or_year": date_value,
        "source_url": realtor_csv_url,
        "inventory_column": yoy_col,
    }



