def normalize_county_name(county_name: str) -> str:
    """Normalize county names while preserving a user-facing County suffix."""
    if not county_name:
        return ""
    name = " ".join(str(county_name).replace(",", " ").split()).strip()
    if name and not name.lower().endswith("county"):
        name = f"{name} County"
    return name


def suggest_local_unemployment_series(state: str, county_name: str):
    """
    Deterministic first-pass mapper.

    This is intentionally conservative: the app only auto-pulls when a known mapping
    or analyst-confirmed manual FRED series ID is available. AI can be added later as
    a suggestion layer, but should not invent the economic value itself.
    """
    state_clean = str(state or "").strip().upper()
    county_clean = normalize_county_name(county_name)
    series_id = LOCAL_UNEMPLOYMENT_FRED_SERIES.get((state_clean, county_clean))

    if not series_id:
        return {
            "ok": False,
            "state": state_clean,
            "county_name": county_clean,
            "series_id": "",
            "source_url": "https://fred.stlouisfed.org/searchresults/?search_type=series&search="
            + f"{county_clean.replace(' ', '+')}+{state_clean}+unemployment+rate",
            "message": "No hard-coded FRED county unemployment series found. Paste a FRED/LAUS series ID below, then approve.",
        }

    return {
        "ok": True,
        "state": state_clean,
        "county_name": county_clean,
        "series_id": series_id,
        "source_url": f"https://fred.stlouisfed.org/series/{series_id}",
        "message": f"Mapped {county_clean}, {state_clean} to FRED/BLS-LAUS series {series_id}.",
    }


def fetch_latest_fred_graph_value(series_id: str):
    """
    Pull latest non-missing observation from FRED's public graph CSV endpoint.
    This endpoint is convenient for Streamlit prototypes and does not require an API key.
    """
    if not series_id:
        raise ValueError("Missing FRED series id.")

    url = f"https://fred.stlouisfed.org/graph/fredgraph.csv?id={series_id}"
    df = pd.read_csv(url)

    if df.empty or series_id not in df.columns:
        raise ValueError(f"No usable FRED data returned for {series_id}.")

    df[series_id] = pd.to_numeric(df[series_id].replace(".", pd.NA), errors="coerce")
    df = df.dropna(subset=[series_id])

    if df.empty:
        raise ValueError(f"FRED series {series_id} has no numeric observations.")

    latest = df.iloc[-1]
    return {
        "series_id": series_id,
        "date": str(latest["observation_date"]),
        "value": float(latest[series_id]),
        "source_url": f"https://fred.stlouisfed.org/series/{series_id}",
    }


def pull_local_unemployment_difference(series_id: str):
    """Pull local unemployment, US unemployment, and calculate local minus US."""
    local = fetch_latest_fred_graph_value(series_id)
    us = fetch_latest_fred_graph_value("UNRATE")

    diff = round(local["value"] - us["value"], 2)

    return {
        "ok": True,
        "local_series_id": series_id,
        "local_date": local["date"],
        "local_unemployment_rate": local["value"],
        "us_date": us["date"],
        "us_unemployment_rate": us["value"],
        "unemployment_rate_difference_vs_us": diff,
        "source_url": local["source_url"],
        "us_source_url": us["source_url"],
    }


